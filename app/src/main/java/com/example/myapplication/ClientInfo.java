package com.example.myapplication;

public class ClientInfo {
    private String name;
    private String clan;
    private int country;
    private String skin;
    private int useCustomColor;
    private int colorBody;
    private int colorFeet;
    private int id;

    // Getters a Setters

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClan() {
        return clan;
    }

    public void setClan(String clan) {
        this.clan = clan;
    }

    public int getCountry() {
        return country;
    }

    public void setCountry(int country) {
        this.country = country;
    }

    public String getSkin() {
        return skin;
    }

    public void setSkin(String skin) {
        this.skin = skin;
    }

    public int getUseCustomColor() {
        return useCustomColor;
    }

    public void setUseCustomColor(int useCustomColor) {
        this.useCustomColor = useCustomColor;
    }

    public int getColorBody() {
        return colorBody;
    }

    public void setColorBody(int colorBody) {
        this.colorBody = colorBody;
    }

    public int getColorFeet() {
        return colorFeet;
    }

    public void setColorFeet(int colorFeet) {
        this.colorFeet = colorFeet;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
